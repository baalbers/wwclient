package org.gvsig.sos.lib.impl.exceptions;

import org.gvsig.sos.lib.api.SOSException;

/**
 * This exception is intended to be used for notifying any exception occurred
 * while creating the SOS client through the manager. 
 *
 * @author @author <a href="mailto:pupo@uji.es"> Luis E. Rodríguez </a>
 */
public class CreationException extends SOSException {

	private static String MESSAGE = "Manager can't create SOS client.";
	private static String KEY = "_Cant_Create_Client_Exception";
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 7594106703133147402L;

	public CreationException(Throwable cause) {
		super(MESSAGE, cause, KEY, serialVersionUID);
	}

}
