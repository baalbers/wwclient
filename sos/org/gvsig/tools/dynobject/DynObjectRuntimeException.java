package org.gvsig.tools.dynobject;

import org.gvsig.tools.exception.BaseRuntimeException;

public class DynObjectRuntimeException extends BaseRuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8715772554233511721L;

	public DynObjectRuntimeException(Throwable cause) {
		super(
			"DynObject error.",
			cause,
			"_DynObject_error", 
			serialVersionUID
		);
	}

	protected DynObjectRuntimeException(String messagefmt, String key, long code) {
		super(messagefmt, key, code);
	}

	protected DynObjectRuntimeException(String messagefmt, Throwable cause, String key, long code) {
		super(messagefmt, cause, key, code);
	}
}
